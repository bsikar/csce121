#include <iostream>
using namespace std;

int main() {
  int numWhosits;
  int numWhatsits;
  int totalCollection;

  cin >> numWhosits;

  numWhatsits = numWhosits;
  numWhosits += 2;
  totalCollection = numWhatsits + numWhosits;

  cout << numWhosits << " whosits" << endl;
  cout << numWhatsits << " whatsits" << endl;
  cout << totalCollection << " total" << endl;

  return 0;
}
